import UIKit

extension UIColor {
    static let primaryBlue: UIColor = {
        return UIColor(red: 7/255, green: 115/255, blue: 186/255, alpha: 1)
    }()

    static let lightGray: UIColor = {
        return UIColor(red: 166/255, green: 166/255, blue: 166/255, alpha: 1)
    }()
}
